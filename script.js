//Cartas Random
  
var cartasNPC
var cartasJogador

function sortearCartas() {
  var nCartasNPC = parseInt(Math.random() * cartas.length);
  cartasNPC = cartas[nCartasNPC]
  var nCartasJogador = parseInt(Math.random() * cartas.length);
  while (nCartasNPC == nCartasJogador) {
    nCartasJogador = parseInt(Math.random() * cartas.length);

  }
  
  cartasJogador = cartas[nCartasJogador]

  document.getElementById("btnSortear").disabled = true;
  document.getElementById("btnJogar").disabled = false
  exibirOpcoes();
  exibirCartaJogador()

}

function exibirOpcoes() {
  var opcoes = document.getElementById("opcoes")
    var opcoesTexto = ""
  for (var atributo in cartasJogador.atributos) {
opcoesTexto += "<input type='radio' name='atributo' value='" + atributo + "'>" + atributo;
  }
  
}

function obtemAtributoSelecionado() {
 var radioAtributos = document.getElementsByName("atributo");
 for (var i = 0; i < radioAtributos.length; i++) {
   if (radioAtributos[i].checked == true) {
     return radioAtributos[i].value 
   } 
 }
}

function jogar() {
 var atributoSelecionado = obtemAtributoSelecionado()
 var divResultado = document.getElementById("resultado")

 if (cartasJogador.atributos[atributoSelecionado] >  cartasNPC.atributos[atributoSelecionado]) { 
  htmlResultado = "<p class='resultado-final'>YOU WIN!</p>"
} else if (cartasJogador.atributos[atributoSelecionado] < cartasNPC.atributos[atributoSelecionado]) { 
  htmlResultado = "<p class='resultado-final'>YOU LOSE!</p>"
} else {
  htmlResultado = "<p class='resultado-final'>DRAWN!</p>" 
}
  divResultado.innerHTML = htmlResultado;
  
    document.getElementById("btnJogarNovamente").disabled = false
  document.getElementById("btnJogar").disabled = true;
  exibirCartaNPC()
   }
  
function exibirCartaJogador() {
  var divCartaJogador = document.getElementById("carta-jogador")
  divCartaJogador.style.backgroundImage= `url(${cartasJogador.imagem})`
//divCartaJogador.style.backgroundImage= "url(" + cartaJogador.imagem + ")"
  var moldura = '<img src="https://www.alura.com.br/assets/img/imersoes/dev-2021/card-super-trunfo-transparent-ajustado.png" style= "width: inherit; height: inherit; position: absolute;">';
  var tagHTML = "<div id='opcoes' class='carta-status'>"
  
  var opcoesTexto = ""
  for (var atributo in cartasJogador.atributos) {
opcoesTexto += "<input type='radio' name='atributo' value='" + atributo + "'>" + atributo + " " +cartasJogador.atributos[atributo] + "<br>" ;
  }
  var nome = `<p class="carta-subtitle">${cartasJogador.nome}</p>`
  
  divCartaJogador.innerHTML = moldura + nome + tagHTML + opcoesTexto + '</div>'
  
}

